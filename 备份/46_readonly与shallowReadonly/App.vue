<template>
  <div class="app">
    <h2>当前sum1为：{{ sum1 }}</h2>
    <h2>当前sum2为：{{ sum2 }}</h2>
    <h2>当前car1为:{{ car1 }}</h2>
    <h2>当前car2为:{{ car2 }}</h2>
    <button @click="addSum1">点击我sum1+1</button>
    <button @click="addSum2">点击我sum2+1</button>
    <br />
    <br />
    <button @click="changeBrand1">修改品牌(car1)</button>
    <button @click="changeColor1">修改颜色(car1)</button>
    <button @click="changePrice1">修改价格(car1)</button>
    <br />
    <br />
    <button @click="changeBrand2">修改品牌(car2)</button>
    <button @click="changeColor2">修改颜色(car2)</button>
    <button @click="changePrice2">修改价格(car2)</button>
  </div>
</template>

<script setup lang="ts" name="App">
  import { ref, readonly, reactive, shallowReadonly } from "vue";
  //数据
  let sum1 = ref(0);
  let sum2 = readonly(sum1);
  let car1 = reactive({
    brand: "奔驰",
    options: {
      color: "红色",
      priice: 500,
    },
  });
  let car2 = shallowReadonly(car1);

  //方法
  function addSum1() {
    sum1.value += 1;
  }
  function addSum2() {
    sum2.value += 1; //sum2 是不能修改的
  }
  function changeColor1() {
    car1.options.color = "绿色";
  }
  function changePrice1() {
    car1.options.priice += 100;
  }
  function changeBrand1() {
    car1.brand = "宝马";
  }
  function changeColor2() {
    car2.options.color = "绿色";
  }
  function changePrice2() {
    car2.options.priice += 100;
  }
  function changeBrand2() {
    car2.brand = "宝马";
  }
</script>

